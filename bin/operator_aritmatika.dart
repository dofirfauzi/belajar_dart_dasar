void main() {
  /*
  OPERATOR ARITMATIKA

  +               -> Pertambahan
  - , -expression -> Pengurangan atau negatif
  *               -> Perkalian
  /               -> Pembagian, hasilnya double
  ~/              -> Pembagian, hasilnya Int
  %               -> Sisa bagi

  */

  var value1 = 10;
  var value2 = 3;

  print(value1 + value2);
  print(value1 - value2);
  print(value1 * value2);
  print(value1 / value2);
  print(value1 ~/ value2);
  print(value1 % value2);

}