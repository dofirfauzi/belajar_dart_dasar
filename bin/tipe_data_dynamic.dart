void main() {
  /*
  TIPE DATA DYNAMIC
  1. kada ada kebutuhan kita ingin membuat variable yang bisa menampung semua janis tipe data
  2. pada kasus ini kita bisa menggunakan tipe data dynamic
  */

  dynamic variable = 100;
  print(variable);

  variable = true;
  print(variable);

  variable = "dynamic";
  print(variable);
}