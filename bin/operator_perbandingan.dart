void main() {
  /*
  OPERATOR PERBANDINGAN
  1. Operator perbandingan adalah operator yang hasilnya bool
  2. Operator perbandingan dapat digunakan di tipe data number dan string

  Berikut adalah operator perbandingan yang didukung oleh dart

  ==      -> Sama Dengan
  !=      -> Tidak Sama Dengan
  >       -> Lebih dari
  <       -> Kurang dari
  >=      -> Lebih dari atau sama dengan
  <=      -> Kurang dari atau sama dengan

  */

  print(20 == 20); //true
  print(20 != 20); //false
  print(20 > 20); //false
  print(20 < 20); //false
  print(20 >= 20); //true
  print(20 <= 20); //true

  print('Dofir' == 'Dofir'); //true
}